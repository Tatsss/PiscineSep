#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}

int	main(void)
{
	int		hoge;
	char	foo[] = "Abc";
	char	bar[] = "pfe";

	hoge = ft_strcmp(foo, bar);
	printf("%d", hoge);
	return (0);
}
