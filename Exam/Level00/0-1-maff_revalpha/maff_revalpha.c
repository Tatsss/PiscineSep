#include <unistd.h>

int	main(void)
{
	int	i;
	int	upper;

	i = 'z';
	while (i >= 'a')
	{
		if (i % 2 == 0)
		{
			upper -= 32;
			write(1, &upper, 1);
		}
		else
			write(1, &i, 1);
	}
	return (0);
}
